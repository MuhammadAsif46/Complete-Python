# Single-line comment example:
# This program prints different outputs to the console.

# Printing a number
print(27)

# Printing a text message
print("Hello, World!")

# Printing the sum of two numbers
print(10 + 5)

# Printing the product of two numbers
print(10 * 10)

# Using f-string to print formatted text with a calculation
print(f'Sum of two numbers: {10 + 5}')

