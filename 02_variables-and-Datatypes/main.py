# Variables and Data Types in Python
# String variable
username = "JohnDoe"  

print(f'My name is {username}')

# Integer variable
age = 25
print("Age:", age)

length = 20

width = 10

# Calculate the area of a rectangle
area = length * width   
print(f'Area of the rectangle: {area}')

# Float variable
price = 19.99
print("Price:", price)

# Boolean variable
is_available = True
print("Is available:", is_available)


num1 = 10
num2 = '5'

# Perform addition
# sum_result = num1 + num2
# print("Sum:", sum_result)
print(type(num1))
print(type(num2))



# Rules:
# Should not start with a number
# Should not contain special characters except underscore (_)
# Should not be a reserved keyword in Python
# Should be descriptive and meaningful
# Should be in lowercase letters
# Should be consistent in naming convention (e.g., snake_case or camelCase)
# Should be unique within the same scope
